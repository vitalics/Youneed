// Generates src/data/packages.ts from packages/*/package.json.
// Also emits src/data/package-examples.ts — the first TS/JS code fence of each
// package's README (rendered as the usage example in the docs package index).
// Run from anywhere: node site/scripts/gen-packages.mjs
import { readdirSync, readFileSync, statSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { join, dirname } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const packagesDir = join(here, "..", "..", "packages");
const outFile = join(here, "..", "src", "data", "packages.ts");
const outExamplesFile = join(here, "..", "src", "data", "package-examples.ts");

/** First ts/tsx/js/javascript/typescript fence of a README, or undefined. */
function extractExample(readmePath) {
  let md;
  try {
    md = readFileSync(readmePath, "utf8");
  } catch {
    return undefined;
  }
  const fence = /^```(ts|tsx|js|jsx|typescript|javascript)\s*$/gm;
  const start = fence.exec(md);
  if (!start) return undefined;
  const from = start.index + start[0].length + 1; // past the opening fence line
  const end = md.indexOf("```", from);
  if (end === -1) return undefined;
  const code = md.slice(from, end).replace(/\n+$/, "");
  // Defensive cap — the index shows usage sketches, not full files.
  const lines = code.split("\n");
  return lines.length > 60 ? lines.slice(0, 60).join("\n") : code;
}

/** Ecosystem bucket, derived from the directory name. */
function categorize(dir) {
  if (dir === "otel" || dir.endsWith("-otel")) return "otel";
  if (dir === "dom" || dir.startsWith("dom-") || dir === "devtools") return "dom";
  if (dir === "server" || dir.startsWith("server-") || dir === "kv" || dir === "kv-redis") return "server";
  if (dir === "ssr" || dir.startsWith("ssr-")) return "ssr";
  if (dir === "cli" || dir.startsWith("cli-")) return "cli";
  if (dir === "test" || dir.startsWith("test-")) return "test";
  if (dir.startsWith("orm-")) return "orm";
  if (dir === "logger" || dir.startsWith("logger-")) return "logger";
  return "core";
}

const entries = [];
for (const dir of readdirSync(packagesDir).sort()) {
  const pj = join(packagesDir, dir, "package.json");
  try {
    if (!statSync(pj).isFile()) continue;
  } catch {
    continue;
  }
  const pkg = JSON.parse(readFileSync(pj, "utf8"));
  entries.push({
    name: pkg.name,
    dir,
    cat: categorize(dir),
    desc: pkg.description ?? "",
  });
}

const banner = `// GENERATED by site/scripts/gen-packages.mjs — do not edit by hand.
// Source of truth: packages/*/package.json descriptions.
`;
const body = `${banner}
export type PackageCategory = "dom" | "server" | "ssr" | "cli" | "test" | "orm" | "logger" | "otel" | "core";

export interface PackageEntry {
  /** npm package name */
  name: string;
  /** directory under packages/ */
  dir: string;
  /** ecosystem bucket */
  cat: PackageCategory;
  /** package.json description */
  desc: string;
}

export const packages: PackageEntry[] = ${JSON.stringify(entries, null, 2)};
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, body);

// Usage examples: dir → first TS/JS code fence of the package README. Kept in a
// SEPARATE module so the docs client bundle (which imports data/packages.ts for
// the sidebar/explorer) never pays for code snippets — only the SSR'd index does.
const examples = {};
let withExamples = 0;
for (const { dir } of entries) {
  const code = extractExample(join(packagesDir, dir, "README.md"));
  if (code) {
    examples[dir] = code;
    withExamples++;
  }
}
const examplesBody = `// GENERATED by site/scripts/gen-packages.mjs — do not edit by hand.
// Source of truth: the first TS/JS code fence of packages/*/README.md.

/** Usage example keyed by package dir (absent when the README has no code fence). */
export const packageExamples: Record<string, string> = ${JSON.stringify(examples, null, 2)};
`;
writeFileSync(outExamplesFile, examplesBody);
console.log(`wrote ${entries.length} packages → ${outFile}`);
console.log(`wrote ${withExamples} usage examples → ${outExamplesFile}`);
